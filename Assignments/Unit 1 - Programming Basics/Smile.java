public class Smile {
    public static void main(String args[]) {
        // PRINT FOREHEAD
        System.out.println("\t\t\t\t   *****   ");
        System.out.println("\t\t\t\t  *     *");

        // PRINT EYEBROWS & EYES
        System.out.println("\t\t\t\t * -   - *");
        System.out.println("\t\t\t\t *  o o  *");

        // PRINT NOSE AND MOUTH
        System.out.println("\t\t\t\t*    !    *");
        System.out.println("\t\t\t\t*    +    *");
        System.out.println("\t\t\t\t * \\\\__/ *");

        // PRINT CHIN
        System.out.println("\t\t\t\t *       *");
        System.out.println("\t\t\t\t   *****  ");
    }
}
